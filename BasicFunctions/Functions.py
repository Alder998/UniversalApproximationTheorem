
import math
import numpy as np
import matplotlib.pyplot as plt

class functions ():
    def __init__ (self):
        pass

    # Let's build a simple continuous function

    # Sine function
    def sineFunction(self, x):
        return math.sin(x)

    # Cosine function
    def cosineFunction(self, x):
        return math.cos(x)

    # Exponential Function
    def exponentialFunction(self, x):
        return math.exp(x)

    # Quadratic function
    def quadraticFunction (self, x):
        return x ** 2
